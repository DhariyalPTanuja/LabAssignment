//1.2 Write a Java program to display the default value of all primitive data types of Java
package com.bridgelabs.problemsday1;
//to display the default value of all primitive data type of Java
public class Day1_2 {
	static int i;
	static float f;
	static double d;
	static char c;
	static boolean b;
	static long l;
	static String s;
public static void main(String[] args) {
	
	System.out.println("default value of primitive data type......");
	System.out.println("integer i =" + i);
	System.out.println("float f = " +f);
	System.out.println("double d =" +d );
	System.out.println("char c=" +c );
	System.out.println("boolean b =" +b );
	System.out.println("long l =" +l );
	System.out.println("string s=" +s );
	
	

}
}
